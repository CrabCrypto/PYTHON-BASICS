#Input

num1 = int(input("Enter first number:"))
num2 = float(input("Enter second number:"))


#Arithmetic operations


sum = num1 + num2
diff = num1 - num2
prod = num1 * num2
div = num1 / num2
rem = num1 % num2

#Display

print(f"SUM = {sum}")
print(f"DIFFERANCE = {diff}")
print(f"PRODUCT = {prod}")
print(f"DIVISION = {div}")
print(f"REMINDER = {rem}")